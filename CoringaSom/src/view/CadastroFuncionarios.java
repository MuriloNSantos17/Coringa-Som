    void cadastrar()
    {
        if(validaCampos() ==true)
       {
           System.out.println("entrou here");
           ModeloUsuario modelUsuario = new ModeloUsuario();
           modelUsuario.setDs_matricula(jTextFieldMatricula.getText());
           modelUsuario.setDs_senha(jPasswordFieldSenha.getText());
           modelUsuario.setId_usuario(jTextFieldUsuario.getText());
           modelUsuario.setNm_usuario(jTextFieldNome.getText());
           ControleUsuario controle_usuario = new ControleUsuario();
           controle_usuario.CadastrarUsuario(modelUsuario);
           
           int id= controle_usuario.buscaMaximoIdUsuario();
           controle_usuario.cadastrarAcesso(id);
           
           limpaCampos();
       }
    }
    void limpaCampos()
    {
        jTextFieldNome.setText("");
        jTextFieldMatricula.setText("");
        jTextFieldPesquisar.setText("");
        jTextFieldUsuario.setText("");
        jPasswordFieldSenha.setText("");
        jPasswordFieldConfirmaSenha.setText("");
        jButtonEditar.setEnabled(false);
        jButtonInativar.setEnabled(false);
        jButtonCadastrar.setEnabled(true);
        jTextFieldNome.requestFocus();
        buscaMatricula();
        preencherTabela("SELECT NM_USUARIO, ID_USUARIO, DS_MATRICULA,"
        + " DS_STATUS FROM USUARIOS");
    }
    
