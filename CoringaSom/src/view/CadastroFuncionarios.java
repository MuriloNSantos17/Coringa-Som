    void buscaMatricula()
    {
        int matricula = metodo.buscaUltimaMatricula();
              
        if(matricula<9)
        {
            jTextFieldMatricula.setText("000"+matricula);
        }
        else if(matricula<99)
        {
            jTextFieldMatricula.setText("00"+matricula);
        }
        else if(matricula<999)
        {
            jTextFieldMatricula.setText("0"+matricula);
        }
        else
        {
            jTextFieldMatricula.setText(matricula+"");
        }
            
    }
    
    public boolean validaCampos()
    {
        String senha = jPasswordFieldSenha.getText();
        String senhaConfirmar = jPasswordFieldConfirmaSenha.getText();
        
       
        if(jTextFieldNome.getText().isEmpty() 
            || jTextFieldUsuario.getText().isEmpty() ||
            (!senha.equals(senhaConfirmar)))
        {
            metodo.validaCampo(jTextFieldNome,"Nome");
            metodo.validaCampo(jTextFieldUsuario,"Usuário");
            metodo.validaCampoSenha(jPasswordFieldSenha,"Senha");
            metodo.validaCampoSenha(jPasswordFieldConfirmaSenha,"Confirmar Senha");
            
            if(!senha.equals(senhaConfirmar))
            {
                 JOptionPane.showMessageDialog(null,
                "Os Campos de senha estão diferentes","Erro:",JOptionPane.ERROR_MESSAGE);
                 jPasswordFieldSenha.setText("");
                 jPasswordFieldConfirmaSenha.setText("");
                 jPasswordFieldSenha.requestFocus();
            }
            
            if(jPasswordFieldConfirmaSenha.getText().isEmpty())
            {
                jPasswordFieldConfirmaSenha.requestFocus();
            }
            if(jTextFieldUsuario.getText().isEmpty())
            {
                jTextFieldUsuario.requestFocus();
            }
            if(jPasswordFieldSenha.getText().isEmpty())
            {
                jPasswordFieldSenha.requestFocus();
            }
            if(jTextFieldNome.getText().isEmpty())
            {
                jTextFieldNome.requestFocus();
            }
           
            return false;
        }
        else
        {
            return true;
        }
    }
   
    
