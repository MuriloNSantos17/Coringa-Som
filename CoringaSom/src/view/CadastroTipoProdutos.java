    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
       String produto= jTableTipoProduto.getValueAt(jTableTipoProduto.getSelectedRow(), 0)+"";
       String id = jTableTipoProduto.getValueAt(jTableTipoProduto.getSelectedRow(), 2)+"";
       
       int opc = JOptionPane.showConfirmDialog(null,"Deseja realmente alterar o produto "+produto+" ?");
       if(opc==JOptionPane.YES_OPTION)
       {
           ModeloTipoProduto modeloTipoProduto = new ModeloTipoProduto();
           modeloTipoProduto.setId(Integer.parseInt(id));
           modeloTipoProduto.setNm_tipo(jTextFieldTipo.getText());
           ControleTipoProduto controleTipoProduto = new ControleTipoProduto();
           controleTipoProduto.alterarTipoProduto(modeloTipoProduto);
           jTextFieldTipo.setText("");
           preencherTabela("SELECT ID, NM_TIPO, DS_STATUS FROM TIPO_PRODUTO ORDER BY NM_TIPO;");
           jButtonEditar.setEnabled(false);
           jButtonInativar.setEnabled(false);
           jButtonCadastrar.setEnabled(true);
       }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonInativarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInativarActionPerformed
      String texto = jButtonInativar.getText();
      String ds_status="";
      
      if(texto.equals("ATIVAR"))
      {
          ds_status = "A";
      }
      else
      {
          ds_status = "I";
      }
      
      String produto= jTableTipoProduto.getValueAt(jTableTipoProduto.getSelectedRow(), 0)+"";
      String id = jTableTipoProduto.getValueAt(jTableTipoProduto.getSelectedRow(), 2)+"";
      
      int opc = JOptionPane.showConfirmDialog(null,"Deseja realmente "+texto+" o produto "+produto+" ?");
      
       if(opc==JOptionPane.YES_OPTION)
       {
           ModeloTipoProduto modeloTipoProduto = new ModeloTipoProduto();
           modeloTipoProduto.setId(Integer.parseInt(id));
           modeloTipoProduto.setDs_status(ds_status);
           ControleTipoProduto controleTipoProduto = new ControleTipoProduto();
           controleTipoProduto.ativarInativarTipo(modeloTipoProduto);
           jTextFieldTipo.setText("");
           preencherTabela("SELECT ID, NM_TIPO, DS_STATUS FROM TIPO_PRODUTO ORDER BY NM_TIPO;");
           jButtonEditar.setEnabled(false);
           jButtonInativar.setEnabled(false);
           jButtonCadastrar.setEnabled(true);
       }
    }//GEN-LAST:event_jButtonInativarActionPerformed

