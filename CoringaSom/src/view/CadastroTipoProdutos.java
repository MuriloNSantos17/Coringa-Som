    private void jTextFieldTipoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldTipoKeyPressed
       if(evt.getKeyCode() == KeyEvent.VK_ENTER && jButtonCadastrar.isEnabled()==true)
       {
            if(!jTextFieldTipo.getText().isEmpty())
            {
                ModeloTipoProduto tipoProduto = new ModeloTipoProduto();
                tipoProduto.setNm_tipo(jTextFieldTipo.getText());
                tipoProduto.setDs_status("A");
                ControleTipoProduto controle = new ControleTipoProduto();
                controle.gravarTipoProduto(tipoProduto);
                preencherTabela("SELECT ID, NM_TIPO, DS_STATUS FROM TIPO_PRODUTO ORDER BY NM_TIPO;");
            }
            else
            {
                MetodosPadrao metodo = new MetodosPadrao();
                metodo.validaCampo(jTextFieldTipo,"Tipo produto");
            }
       }
    }//GEN-LAST:event_jTextFieldTipoKeyPressed

    void cadastrar()
    {
         if(!jTextFieldTipo.getText().isEmpty())
        {
            ModeloTipoProduto tipoProduto = new ModeloTipoProduto();
            tipoProduto.setNm_tipo(jTextFieldTipo.getText());
            tipoProduto.setDs_status("A");
            ControleTipoProduto controle = new ControleTipoProduto();
            controle.gravarTipoProduto(tipoProduto);
            preencherTabela("SELECT ID, NM_TIPO, DS_STATUS FROM TIPO_PRODUTO ORDER BY NM_TIPO;");
            
        }
        else
        {
            MetodosPadrao metodo = new MetodosPadrao();
            metodo.validaCampo(jTextFieldTipo,"Tipo produto");
        }
        limparCampos();
    }
    
    void limparCampos()
    {
        jTextField6.requestFocus();
        jTextFieldTipo.setText("");
            jTextField6.setText("");
            jTextFieldTipo.requestFocus();
    }
    public void preencherTabela(String SQL) 
    {

       //MUDANDO A FONTE DO CABEÇALHO
       JTableHeader cabecalho = jTableTipoProduto.getTableHeader();
       cabecalho.setFont(new java.awt.Font("Arial", 1, 12));

       ArrayList dados = new ArrayList();

       Conecta_Banco conecta = new Conecta_Banco();
       conecta.conexao();

       String[] colunas = new String[]{"TIPO","STATUS","ID"};

       conecta.executaSQL(SQL);
       try {
           conecta.rs.first();
           do {
               String status = conecta.rs.getString("DS_STATUS");

               
                if(status.equals("A"))                
                {
                    status="ATIVO";
                }
                else
                {
                    status="INATIVO";
                }
              dados.add(new Object[]{conecta.rs.getString("NM_TIPO").toUpperCase(),                   
              status,conecta.rs.getInt("id")});

           } while (conecta.rs.next());
       } catch (SQLException ex) {

           JOptionPane.showMessageDialog(null,"Nenhum dado Encontrado!");
           System.out.println(ex);

       }

       jTableTipoProduto.getTableHeader().setReorderingAllowed(false);
       jTableTipoProduto.setAutoResizeMode(jTableTipoProduto.AUTO_RESIZE_OFF);
       jTableTipoProduto.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

       JTableHeader header =  jTableTipoProduto.getTableHeader();
       DefaultTableCellRenderer centralizado = (DefaultTableCellRenderer) header.getDefaultRenderer();
       centralizado.setHorizontalAlignment(SwingConstants.CENTER);

       Conecta_Tabela ct = new Conecta_Tabela(dados, colunas);
       jTableTipoProduto.setModel(ct);



       jTableTipoProduto.getColumnModel().getColumn(0).setPreferredWidth(375);
       jTableTipoProduto.getColumnModel().getColumn(0).setResizable(true);

       jTableTipoProduto.getColumnModel().getColumn(1).setPreferredWidth(375);
       jTableTipoProduto.getColumnModel().getColumn(1).setResizable(true);

       jTableTipoProduto.getColumnModel().getColumn(2).setPreferredWidth(375);
       jTableTipoProduto.getColumnModel().getColumn(2).setResizable(true);

       //Ocultando a Coluna ID
       jTableTipoProduto.getColumnModel().getColumn(2).setMinWidth(0);
       jTableTipoProduto.getColumnModel().getColumn(2).setMaxWidth(0);

       jTableTipoProduto.getTableHeader().setReorderingAllowed(false);
       jTableTipoProduto.setAutoResizeMode(jTableTipoProduto.AUTO_RESIZE_OFF);
       jTableTipoProduto.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

   }


